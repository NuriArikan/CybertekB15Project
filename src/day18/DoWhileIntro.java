package day18;

public class DoWhileIntro {
    public static void main(String[] args) {
        // [] square bracket
        // {} curly braces
        // () parenthesis
        // ; semi colon
        // : colon
        // % remainder operator
        // ! exclamation mark
        // | pipe
        // & Ampersand
        // \ backslash
        // / forward slash
        // " double quote
        // ' single quote

        int x = 1;

        do {
            System.out.println( x );
            ++x;
        } while ( x <= 5) ;   // semi colon ; is mandatory


        int y = 5 ;

        do {
            System.out.println( y );
            --y;
        } while ( y > 0);



    }
}
