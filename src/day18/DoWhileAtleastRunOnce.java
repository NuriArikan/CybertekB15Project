package day18;

public class DoWhileAtleastRunOnce {
    public static void main(String[] args) {

        int x = 1000;

        do{
            System.out.println("Keep Going");
        } while ( x < 5 );

        System.out.println("------");

        System.out.println("The End");

        while (x<5){
            System.out.println("Keeping going in while");
        }



    }
}
