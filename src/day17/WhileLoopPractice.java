package day17;

public class WhileLoopPractice {
    public static void main(String[] args) {

        int count1 = 1;
        int count2 = 5;

        while (count1<=5){
            System.out.println("count1 = " + count1 );
            count1++;

        }

        while (count2>0){
            System.out.println("count2 = " + count2);
            --count2;
        }





    }
}
